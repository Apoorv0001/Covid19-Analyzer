       wearing masks, physically distancing and avoiding crowds. Being vaccinated does not mean that<br> we can 
       throw caution to the wind and put ourselves and others at risk, particularly because it is still not 
       clear the degree to which the vaccines can protect not only<br>against disease but also against infection 
