    <h2>COVISHEILD</h2>
    <p>Covishield has been developed by Oxford University in collaboration with pharma<br> major 
       Astrazeneca.India’s Serum Institute is their manufacturing and trial partner.
    Covishield uses a replication-deficient chimpanzee viral vector based on a<br> weakened version 
    of a common cold virus that causes infections in chimpanzees<br> and contains the genetic material 
    of the SARS-CoV-2 virus spike protein. After<br> vaccination, the surface spike protein is produced, priming 
    the immune system<br>to attack the SARS-CoV-2 virus if it later infects the body.</p>
